// APPLICATION API CONFIGURATION

// frontend/src/config/api.js
import axios from 'axios';

// Base API URL - in development, use relative path (proxy will handle forwarding to backend)
// The proxy in vite.config.js will forward /api requests to http://localhost:5000
const BASE_URL = '/api';

// Create axios instance
const api = axios.create({
  baseURL: BASE_URL,
  timeout: 10000,
  headers: {
    'Content-Type': 'application/json'
  }
});

// Request interceptor for logging
api.interceptors.request.use(
  (config) => {
    console.log(`ğŸ“¤ API Request: ${config.method?.toUpperCase()} ${config.url}`);
    return config;
  },
  (error) => {
    console.error('âŒ Request Error:', error);
    return Promise.reject(error);
  }
);

// Response interceptor for error handling
api.interceptors.response.use(
  (response) => {
    console.log(`âœ… API Response: ${response.config.url}`);
    return response;
  },
  (error) => {
    console.error('âŒ Response Error:', error.response?.data || error.message);
    return Promise.reject(error);
  }
);

export default api;